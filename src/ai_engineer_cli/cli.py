import argparse
import sys

from ai_engineer_cli.terminal_ui import print_cli_response
from ai_engineer_cli.agent import (
    Agent,
    ConversationConfig,
    FactsManager,
    MessageStore,
    SummaryManager,
)
from ai_engineer_cli.config import load_config
from ai_engineer_cli.llm_client import LLMClient
from ai_engineer_cli.prompt_templates import (
    list_templates,
    load_template,
    render_template,
)
from ai_engineer_cli.response_format import (
    ResponseFormat,
    ResponseLanguage,
    build_system_prompt,
    validate_json_response,
)


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="ai-engineer-cli",
        description="CLI tool for working with LLMs as an engineering assistant.",
    )

    parser.add_argument(
        "prompt",
        nargs="?",
        type=str,
        help="Prompt to send to the LLM.",
    )

    parser.add_argument(
        "--format",
        choices=[item.value for item in ResponseFormat],
        default=None,
        help="Response format: text, markdown, or json.",
    )

    parser.add_argument(
        "--language",
        choices=[item.value for item in ResponseLanguage],
        default=None,
        help="Response language: ru or en.",
    )

    parser.add_argument(
        "--max-output-tokens",
        type=int,
        default=None,
        help="Maximum number of output tokens generated by the model.",
    )

    parser.add_argument(
        "--stop-instruction",
        type=str,
        default=None,
        help="Instruction that tells the model how to finish the answer.",
    )

    parser.add_argument(
        "--template",
        type=str,
        default=None,
        help="Prompt template name from the prompts directory.",
    )

    parser.add_argument(
        "--list-templates",
        action="store_true",
        help="List available prompt templates and exit.",
    )

    parser.add_argument(
        "--no-separator",
        action="store_true",
        help="Print raw response without visual separators.",
    )

    parser.add_argument(
        "--no-metadata",
        action="store_true",
        help="Do not print response metadata such as model, duration, tokens, and estimated cost.",
    )

    parser.add_argument(
        "--temperature",
        type=float,
        default=None,
        help="Sampling temperature. Lower values are more deterministic; higher values are more diverse.",
    )

    parser.add_argument(
        "--model",
        type=str,
        default=None,
        help="Model name. Overrides OPENAI_MODEL from .env.",
    )

    parser.add_argument(
        "--agent",
        action="store_true",
        default=None,
        help="Run request through Agent runtime instead of direct LLMClient call.",
    )

    parser.add_argument(
        "--conversation-id",
        type=str,
        default="default",
        help="Conversation id for persistent agent history.",
    )

    parser.add_argument(
        "--clear-history",
        action="store_true",
        help="Clear stored history for the selected conversation and exit.",
    )

    parser.add_argument(
        "--show-context-stats",
        action="store_true",
        default=None,
        help="Show estimated token usage for agent context.",
    )

    parser.add_argument(
        "--context-token-limit",
        type=int,
        default=None,
        help="Warn when estimated full context tokens exceed this limit.",
    )

    parser.add_argument(
        "--use-summary",
        action="store_true",
        default=None,
        help="Enable summary-based context compression for agent mode.",
    )

    parser.add_argument(
        "--recent-messages",
        type=int,
        default=None,
        help="Number of recent messages to keep as-is when summary compression is enabled.",
    )

    parser.add_argument(
        "--summary-every",
        type=int,
        default=None,
        help="Compress history into summary when stored message count reaches this value.",
    )

    parser.add_argument(
        "--init-conversation",
        type=str,
        default=None,
        help="Create or update config for a conversation and exit.",
    )

    parser.add_argument(
        "--show-conversation-config",
        action="store_true",
        help="Show stored config for the selected conversation and exit.",
    )

    parser.add_argument(
        "--delete-conversation",
        action="store_true",
        help="Delete the selected conversation file, including config, summary, and messages.",
    )

    parser.add_argument(
        "--context-strategy",
        choices=["full", "summary", "sliding-window", "sticky-facts", "branching"],
        default=None,
        help="Context management strategy.",
    )

    parser.add_argument(
        "--branch",
        type=str,
        default=None,
        help="Branch id for branching strategy.",
    )

    parser.add_argument(
        "--create-branch",
        type=str,
        default=None,
        help="Create a new branch for the selected conversation and exit.",
    )

    parser.add_argument(
        "--from-branch",
        type=str,
        default="main",
        help="Source branch id when creating a new branch.",
    )

    parser.add_argument(
        "--list-branches",
        action="store_true",
        help="List branches for the selected conversation and exit.",
    )

    return parser


def print_response(
    response: str,
    use_separator: bool = True,
    metadata: dict[str, str] | None = None,
    show_metadata: bool = True,
) -> None:
    if not use_separator:
        print(response)

        if metadata and show_metadata:
            print()
            print_metadata(metadata)

        return

    separator = "─" * 60

    print()
    print(separator)
    print("AI RESPONSE")
    print(separator)
    print()
    print(response)
    print()

    if metadata and show_metadata:
        print(separator)
        print("METADATA")
        print(separator)
        print_metadata(metadata)
        print()

    print(separator)


def print_metadata(metadata: dict[str, str]) -> None:
    for key, value in metadata.items():
        print(f"{key}: {value}")

def pick(cli_value, config_value, default_value):
    if cli_value is not None:
        return cli_value

    if config_value is not None:
        return config_value

    return default_value


def print_conversation_config(
    conversation_id: str,
    config: ConversationConfig,
) -> None:
    print(f"conversation_id: {conversation_id}")

    for key, value in config.to_dict().items():
        print(f"{key}: {value}")


def build_conversation_config_from_args(args) -> ConversationConfig:
    context_strategy = args.context_strategy

    if context_strategy is None and args.use_summary:
        context_strategy = "summary"

    return ConversationConfig(
        agent=True if args.agent is None else args.agent,
        response_format=args.format or "markdown",
        language=args.language or "ru",
        show_context_stats=(
            True if args.show_context_stats is None else args.show_context_stats
        ),
        use_summary=True if args.use_summary is None else args.use_summary,
        summary_every=args.summary_every or 10,
        recent_messages=args.recent_messages or 6,
        context_token_limit=args.context_token_limit,
        context_strategy=context_strategy or "full",
        branch_id=args.branch or "main",
        model=args.model,
        max_output_tokens=args.max_output_tokens,
        temperature=args.temperature,
        stop_instruction=args.stop_instruction,
    )

def main() -> None:
    parser = build_parser()
    args = parser.parse_args()

    try:
        if args.list_templates:
            templates = list_templates()

            if not templates:
                print("No templates found.")
                return

            print("Available templates:")
            for template in templates:
                print(f"- {template}")

            return

        conversation_id = args.init_conversation or args.conversation_id
        message_store = MessageStore(conversation_id=conversation_id)
        stored_config = message_store.load_config()

        if args.init_conversation:
            conversation_config = build_conversation_config_from_args(args)
            message_store.save_config(conversation_config)

            print(f"Conversation config saved: {args.init_conversation}")
            print_conversation_config(args.init_conversation, conversation_config)
            return

        if args.show_conversation_config:
            print_conversation_config(conversation_id, stored_config)
            return

        if args.delete_conversation:
            message_store.delete()
            print(f"Conversation deleted: {conversation_id}")
            return

        if args.list_branches:
            branches = message_store.list_branches()

            if not branches:
                print("No branches found.")
                return

            print(f"Branches for conversation: {conversation_id}")
            for branch in branches:
                print(f"- {branch}")

            return

        if args.create_branch:
            message_store.create_branch(
                branch_id=args.create_branch,
                from_branch_id=args.from_branch,
            )

            print(
                f"Branch created: {args.create_branch} "
                f"from {args.from_branch} "
                f"in conversation {conversation_id}"
            )
            return

        effective_agent = pick(args.agent, stored_config.agent, False)
        effective_format = pick(args.format, stored_config.response_format, ResponseFormat.TEXT.value)
        effective_language = pick(args.language, stored_config.language, ResponseLanguage.RU.value)
        effective_show_context_stats = pick(
            args.show_context_stats,
            stored_config.show_context_stats,
            False,
        )
        effective_use_summary = pick(args.use_summary, stored_config.use_summary, False)
        effective_summary_every = pick(args.summary_every, stored_config.summary_every, 10)
        effective_recent_messages = pick(args.recent_messages, stored_config.recent_messages, 6)
        effective_context_token_limit = pick(
            args.context_token_limit,
            stored_config.context_token_limit,
            None,
        )
        effective_model = pick(args.model, stored_config.model, None)
        effective_max_output_tokens = pick(
            args.max_output_tokens,
            stored_config.max_output_tokens,
            None,
        )
        effective_temperature = pick(args.temperature, stored_config.temperature, None)
        effective_stop_instruction = pick(
            args.stop_instruction,
            stored_config.stop_instruction,
            None,
        )

        effective_context_strategy = pick(
            args.context_strategy,
            stored_config.context_strategy,
            "full",
        )

        if effective_context_strategy == "summary":
            effective_use_summary = True

        effective_branch_id = pick(args.branch, stored_config.branch_id, "main")

        if not args.prompt and not args.clear_history:
            raise ValueError(
                "Prompt is required unless --list-templates, --init-conversation, "
                "--show-conversation-config, --delete-conversation, or --clear-history is used."
            )

        if effective_max_output_tokens is not None and effective_max_output_tokens <= 0:
            raise ValueError("--max-output-tokens must be greater than 0.")

        if effective_temperature is not None and not 0 <= effective_temperature <= 2:
            raise ValueError("--temperature must be between 0 and 2.")

        if effective_context_token_limit is not None and effective_context_token_limit <= 0:
            raise ValueError("--context-token-limit must be greater than 0.")

        if effective_show_context_stats and not effective_agent:
            raise ValueError("--show-context-stats can only be used with agent mode.")

        if effective_context_token_limit is not None and not effective_agent:
            raise ValueError("--context-token-limit can only be used with agent mode.")

        if effective_use_summary and not effective_agent:
            raise ValueError("--use-summary can only be used with agent mode.")

        if effective_recent_messages <= 0:
            raise ValueError("--recent-messages must be greater than 0.")

        if effective_summary_every <= 0:
            raise ValueError("--summary-every must be greater than 0.")

        if effective_use_summary and effective_recent_messages >= effective_summary_every:
            raise ValueError("--recent-messages must be less than --summary-every.")

        valid_context_strategies = {
            "full",
            "summary",
            "sliding-window",
            "sticky-facts",
            "branching",
        }

        if effective_context_strategy not in valid_context_strategies:
            raise ValueError(f"Unsupported context strategy: {effective_context_strategy}")

        if effective_context_strategy != "branching" and args.branch is not None:
            raise ValueError("--branch can only be used with --context-strategy branching.")

        if args.create_branch and not args.conversation_id:
            raise ValueError("--create-branch requires --conversation-id.")

        if args.list_branches and not args.conversation_id:
            raise ValueError("--list-branches requires --conversation-id.")

        response_format = ResponseFormat(effective_format)
        response_language = ResponseLanguage(effective_language)

        system_prompt = build_system_prompt(
            response_format=response_format,
            language=response_language,
            max_output_tokens=effective_max_output_tokens,
            stop_instruction=effective_stop_instruction,
        )

        prompt = args.prompt

        if args.template:
            template_text = load_template(args.template)
            prompt = render_template(
                template_text=template_text,
                user_input=args.prompt,
            )

        config = load_config()
        llm_client = LLMClient(config)

        context_token_stats = None

        if effective_agent:
            summary_manager = SummaryManager(llm_client=llm_client) if effective_use_summary else None
            facts_manager = (
                FactsManager(llm_client=llm_client)
                if effective_context_strategy == "sticky-facts"
                else None
            )

            agent = Agent(
                llm_client=llm_client,
                system_prompt=system_prompt,
                message_store=message_store,
                summary_manager=summary_manager,
                facts_manager=facts_manager,
                use_summary=effective_use_summary,
                recent_messages=effective_recent_messages,
                summary_every=effective_summary_every,
                context_strategy=effective_context_strategy,
                branch_id=effective_branch_id,
            )

            if args.clear_history:
                agent.clear_history()
                print(f"History cleared for conversation: {conversation_id}")
                return

            llm_response = agent.run(
                user_input=prompt,
                model=effective_model,
                max_output_tokens=effective_max_output_tokens,
                temperature=effective_temperature,
            )

            context_token_stats = agent.last_context_token_stats

        else:
            llm_response = llm_client.ask(
                prompt=prompt,
                system_prompt=system_prompt,
                model=effective_model,
                max_output_tokens=effective_max_output_tokens,
                temperature=effective_temperature,
            )

        if response_format == ResponseFormat.JSON:
            validate_json_response(llm_response.text)

        metadata = {
            "mode": "agent" if effective_agent else "direct",
            "model": llm_response.model,
            "duration": f"{llm_response.duration_seconds:.2f}s",
            "tokens": (
                f"{llm_response.input_tokens} input / "
                f"{llm_response.output_tokens} output / "
                f"{llm_response.total_tokens} total"
            ),
            "estimated cost": (
                f"${llm_response.estimated_cost_usd:.8f}"
                if llm_response.estimated_cost_usd is not None
                else "unknown"
            ),
        }

        if effective_agent:
            metadata["conversation_id"] = conversation_id
            metadata["summary enabled"] = str(effective_use_summary)
            metadata["context strategy"] = effective_context_strategy

            if effective_use_summary:
                metadata["recent messages"] = str(effective_recent_messages)
                metadata["summary every"] = str(effective_summary_every)

            if effective_context_strategy == "branching":
                metadata["branch"] = effective_branch_id

            if effective_context_strategy == "sticky-facts":
                metadata["facts enabled"] = "True"

        if effective_show_context_stats and context_token_stats:
            metadata.update(
                {
                    "context messages": str(context_token_stats.full_context_messages_count),
                    "history messages": str(context_token_stats.history_messages_count),
                    "estimated current request tokens": str(
                        context_token_stats.current_request_tokens
                    ),
                    "estimated history tokens": str(context_token_stats.history_tokens),
                    "estimated full context tokens": str(
                        context_token_stats.full_context_tokens
                    ),
                }
            )

        if effective_context_token_limit is not None and context_token_stats:
            if context_token_stats.full_context_tokens > effective_context_token_limit:
                metadata["context warning"] = (
                    f"estimated context tokens exceeded limit "
                    f"({context_token_stats.full_context_tokens} > "
                    f"{effective_context_token_limit})"
                )

        print_cli_response(
            response=llm_response.text,
            metadata=metadata,
            user_input=prompt,
            mode="agent" if effective_agent else "direct",
            use_boxes=not args.no_separator,
            show_metadata=not args.no_metadata,
        )

    except Exception as error:
        print(f"Error: {error}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()